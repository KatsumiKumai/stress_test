function GetTime(){
   // 現在のローカル時間が格納された、Date オブジェクトを作成する
   var date_obj = new Date();
   // 秒を現地時間で取得する
   var year = date_obj.getYear()+1900;
   var month = date_obj.getMonth()+1;
   var day = date_obj.getDate();
   var hour = date_obj.getHours();
   var minute = date_obj.getMinutes();
   var second = date_obj.getSeconds();
   var milli = date_obj.getMilliseconds();

   var nowtime = year+"-"+month+"-"+day+" "+hour+":"+minute+":"+second+"."+milli;

   return nowtime;
}